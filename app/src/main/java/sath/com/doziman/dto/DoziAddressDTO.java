public class DoziAddressDTO {

    private String doziId;
    private double doziLatitude;
    private double doziLongitude;
    private String doziName;
    private String doziAddress;
    private String doziMobile;
    private String doziDistance;
    public double getDoziLatitude() {
        return doziLatitude;
    }

    public void setDoziLatitude(double doziLatitude) {
        this.doziLatitude = doziLatitude;
    }

    public double getDoziLongitude() {
        return doziLongitude;
    }

    public void setDoziLongitude(double doziLongitude) {
        this.doziLongitude = doziLongitude;
    }

    public String getDoziName() {
        return doziName;
    }

    public void setDoziName(String doziName) {
        this.doziName = doziName;
    }

    public String getDoziAddress() {
        return doziAddress;
    }

    public void setDoziAddress(String doziAddress) {
        this.doziAddress = doziAddress;
    }

    public String getDoziMobile() {
        return doziMobile;
    }

    public void setDoziMobile(String doziMobile) {
        this.doziMobile = doziMobile;
    }
    public String getDoziDistance() {
        return doziDistance;
    }

    public void setDoziDistance(String doziDistance) {
        this.doziDistance = doziDistance;
    }

    public String getDoziId() {
        return doziId;
    }

    public void setDoziId(String doziId) {
        this.doziId = doziId;
    }

}
